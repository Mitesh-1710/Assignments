//Program To Implement Mathematical Operation Functions.
function addtion(a,b){
    return a + b;
}

function minus(a,b){
    return a - b;
}

function divide(a,b){
    return a / b;
}

function multipy(a,b){
    return a * b;
}

function wholeSquare(a,b){
    return (a + b)**2;
}

console.log("3 + 4 = " , addtion(3,4));
console.log("10 - 4 = " , minus(10,4));
console.log("8 / 2 = " , divide(8,2));
console.log("2.5 * 10 = " , multipy(2.5,10));
console.log("(2 + 10) ^ 2 = " , wholeSquare(2,10));
